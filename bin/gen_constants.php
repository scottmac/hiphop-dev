    $value = preg_replace('/\r/', "\r", $value);
    $value = preg_replace('/\n/', "\n", $value);

    print "'$value'";
  } else if (is_numeric($value)) {
    print $value;
  } else if (is_bool($value)) {
    print $value ? "true" : "false";
  } else if (is_null($value)) {
    print 'null';
  } else {
    exit('doh');
  }
  print ");\n";
}

